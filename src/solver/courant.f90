subroutine courant
  use parameters
  use commons
  use units
  implicit none

  integer :: i,idust
  if(static)then
     return
  endif
  dt=2d44
 
  do i = first_active,last_active
     dt = min(dt,CFL*dx(i)/(cs(i)+abs(q(i,iv))))   
#if GRAVITY==1     
     dt = min(dt,CFL*dx(i)/sqrt(Mc(i)/sqrt(r_c(i)**2.+(l_soft/unit_l)**2.)))
#endif     
#if NDUST>0     
     do idust=1,ndust
        dt=min(dt,CFL*dx(i)/abs(cs(i)+q(i,ivd(idust))))
     end do
#endif     
  end do
  
end subroutine courant
